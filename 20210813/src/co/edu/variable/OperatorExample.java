package co.edu.variable;

public class OperatorExample {
	public static void main(String[] args) {
		// 나머지: %
		// 6/4 => 몫: 1, 나머지: 2
		int result = 6 / 4;
		int result2 = 6 % 4;
		int val = 5;
		if (val % 2 == 0) {
			System.out.println("짝수입니다.");
		} else {
			System.out.println("홀수입니다.");
		}
		
		// 75300 원.
		// 
		// 5만원 => ?개, 1만원 => ?개, 5천원 => ?개
		// 1천원 => ?개, 5백원 => ?개, 1백원 => ?개
		
		// 75300원 5만원 1개, 1만원 2개, 5천원 1개, 1백원 3개.
		
	}
}
